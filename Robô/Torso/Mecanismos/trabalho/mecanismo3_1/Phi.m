function F=Phi(x,t)
l1 = 5;
l2 = 3;
F(1) = x(10);
F(2) = x(11);
F(3) = x(2);
F(4) = x(4);
F(5) = x(3);
F(6) = x(6);
F(7) = x(9);
F(8) = x(8) - x(5);
F(9) = x(7) - x(1);
F(10) = x(10) - x(1) + x(13)*cos(x(12) + x(14)) + l1*cos(x(3));
F(11) = x(11) - x(2) + x(13)*sin(x(12) + x(14)) + l1*sin(x(3));
F(12) = x(10) - x(4) + x(15)*cos(x(12) + x(16)) + l2*sin(x(6));
F(13) = x(11) - x(5) + x(15)*sin(x(12) + x(16)) - l2*cos(x(6));
F(14) = x(7) - 2*l1;
F(15) = x(8) + 2*l2;
F(16) = x(12) - t;
F=F';


