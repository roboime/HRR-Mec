function F=Phi(x,t)
l1 = 5;
l2 = 5;
F(1) = x(1);
F(2) = x(2);
F(3) = x(4);
F(4) = x(5);
F(5) = x(13) - l1/2;
F(6) = x(14);
F(7) = x(16) + l1/2;
F(8) = x(17);
F(9) = x(1) - x(7) + l1*cos(x(3));
F(10) = x(2) - x(8) + l1*sin(x(3));
F(11) = x(4) - x(10) - l1*cos(x(6));
F(12) = x(4) - x(11) - l1*sin(x(6));
F(13) = x(7) - x(10) - l2*sin(x(9)) + l2*sin(x(12));
F(14) = x(8) - x(11) + l2*cos(x(9)) - l2*cos(x(12));
F(15) = x(13) - x(7) + x(19)*cos(x(15) + x(20));
F(16) = x(14) - x(8) + x(19)*sin(x(15) + x(20));
F(17) = x(16) - x(10) + x(21)*cos(x(18) + x(22));
F(18) = x(17) - x(11) + x(21)*sin(x(18) + x(22));
F(19) = x(15) + t;
F(20) = x(18) - t;
F(21) = x(10) - l2*sin(x(12)) - (l1*cos(t))/10;
F(22) = x(11) + l2*cos(x(12)) - (l1 + l2)/2 - (l1*sin(t))/10;
F=F';
